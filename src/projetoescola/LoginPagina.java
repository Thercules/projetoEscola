    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String un = user.getText();
        String p = pass.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3312/escola", "root", "");
            Statement st = (Statement) conn.createStatement();
            String sql = "select * from usuario_login";
            ResultSet rs= st.executeQuery(sql);
            
            while(rs.next()) {
                String usuario=rs.getString("usuario");
                String senha=rs.getString("senha");
                
                if(un.equals(usuario) && p.equals(senha)) {
                    new obrigado ().setVisible(true);                  
                } else {
                    JOptionPane.showMessageDialog(this, "usuario ou senha incorreta");
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro ao conectar com o banco de dados");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

